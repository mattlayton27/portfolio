                    <h3>Site Name</h3>
                        <p>www.rainieradventures.com</p>
                    <h3>Site Purpose Statement</h3>
                        <p>The purpose of this website will be to inform individuals looking for a fun adventure in Mount Rainier. Many of the adventures range from mountain biking, hiking, and backcountry skiing, which will be available to various different levels of expertise. This website will also give a list of guides for those who do not feel comfortable with experiencing these adventures alone.</p>
                    <h3>Site Target Audience</h3>
                        <p>In my website, any between the ages of 18 and 99 will be able to use the resources listed on this website. If the ages are between 0 and 17, those individuals will need to be accompanied by an adult to experience these adventures.  The people using my website can be anyone with a smartphone, tablet, computer, and even dial up internet. The audience will not have a specific education range because anyone can find help from it. </p>
