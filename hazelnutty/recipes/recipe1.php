                    <h3>Ingredients</h3>
                    <ul>
                        <li>1½ cups flour</li>
                        <li>1 cup sugar</li>
                        <li>½ tsp. baking soda</li>
                        <li>½ tsp. salt</li>
                        <li>¾ cup shortening</li>
                        <li>2 large mashed ripe bananas</li>
                        <li>1 egg</li>
                        <li>1¾ cup oats</li>
                        <li>¼ cup Nutella</li>
                    </ul>
                    <h3>Instructions</h3>
                    <ol>
                        <li>Preheat oven to 400 degrees F</li>
                        <li>Sift flour, sugar, baking soda and salt together in a large bowl.</li>
                        <li>Cut in shortening</li>
                        <li>Beat in mashed ripe bananas and egg.</li>
                        <li>Mix in oats.</li>
                        <li>Take a knife and "slice" Nutella into batter with a knife. Don't mix it in completely,
                            the stripes are what make them pretty!</li>
                        <li>Spoon "drops" onto a greased cookie sheet. Bake at 400 degrees F for about 8-10 minutes.
                            (Note: this is a lot softer than normal cookie dough and will not form balls, hence spoon
                            “drops”)</li>
                        <li>You can frost these cookies if you would like too! Here is my easy buttercream recipe:
                            ½ cup butter-softened, 3 Tbsp. milk, 2 tsp. vanilla, 1 box (16 oz.) powdered sugar. Beat
                            all ingredients together. You can add more milk or powdered sugar to get it to the
                            consistency you want it.</li>
                    </ol>
