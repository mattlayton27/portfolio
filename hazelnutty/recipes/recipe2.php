                    <h3>Instructions:</h3>
                    <ol>
                        <li>Heat the oven to 350 degrees. Line a 12-cup mini muffin pan with paper or foil liners.</li>
                        <li>Put the Nutella and egg in a medium bowl and whisk until smooth and well blended.
                            Add the flour and whisk until blended.</li>
                        <li>Spoon the batter into the prepared muffin tins (about ¾ full) and sprinkle with the
                            chopped hazelnuts.</li>
                        <li>Bake until a pick comes out with wet, gooey crumbs, 11 to 12 minutes. Set on a rack
                            to cool completely. Serve immediately or cover and store at room temperature for up to 3 days.</li>
                    </ol>
