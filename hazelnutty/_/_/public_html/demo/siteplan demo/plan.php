                <h1>Site Plan</h1>
                <h2>Site Name</h2>
                <p>MySite.net</p>

                <h2>Purpose</h2>
                <p>This is a teaching and learning site to demonstrate site planning, HTML5 construction techniques and CSS styling approaches. In addition, usability and responsive approaches for modern web use will be employed.</p>
                <h2>Audience</h2>
                <p>The primary audience is college students, ages 16 to 100, who have an interest is learning the essentials of modern web technology. These students understand that the web moves quickly and so their technology will be modern, typically a laptop less than four years old, most will also carry a tablet and/or smartphone. Income levels will be between $10,000 and $100,000 annually. All will have a desire and ability to learn with minimal direction.</p>
